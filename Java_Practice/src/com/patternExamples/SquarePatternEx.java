package com.patternExamples;


class SquareNumEx{
	
	 public static void main(String[] args) {
		 
		 for(int i=0;i<=5;i++) {
			 for(int j=0;j<=5;j++) {
				 System.out.print(j+" ");
			 }
			 System.out.println();
		 }
		
	}
}

public class SquarePatternEx {
	
	public static void main(String[] args) {
		
		for(int i=1;i<=6;i++) {
			for(int j=1;j<=6;j++) {
				System.out.print("*");
			}
			System.out.println();
		}
	}

}
