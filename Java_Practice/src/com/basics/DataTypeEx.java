package com.basics;

public class DataTypeEx {
	
	public static void main(String[] args) {
		
		int bytes = 8;
		
		System.out.println("Byte Size   : "+Byte.SIZE/bytes+" Bytes");
		System.out.println("Short Size  : "+Short.SIZE/bytes+" Bytes");
		System.out.println("Char Size   : "+Character.SIZE/bytes+" Bytes");
		System.out.println("Integer Size: "+Integer.SIZE/bytes+" Bytes");
		System.out.println("Float Size  : "+Float.SIZE/bytes+" Bytes");
		System.out.println("Long Size   : "+Long.SIZE/bytes+" Bytes");
		System.out.println("Double Size : "+Double.SIZE/bytes+" Bytes");
		
	}

}
