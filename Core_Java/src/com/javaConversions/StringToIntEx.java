package com.javaConversions;

public class StringToIntEx {
	public static void main(String[] args) {
		String s="400";
		//to convert String to Integer
		int i=Integer.parseInt(s);
		System.out.println(s+100);//this is String concatenation
		System.out.println(i+200);//this is binary plus operator
		System.out.println("------------------------------------------>");	
	}
}
