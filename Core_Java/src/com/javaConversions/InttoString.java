package com.javaConversions;

public class InttoString {
	public static void main(String[] args) {
		int i=200;
		//to convert integer to String converter
		String s=String.valueOf(i);
		System.out.println(i+10);//this is binary operator 200+10=210
		System.out.println(s+20);//this is String concatenation 20020
	}
}
