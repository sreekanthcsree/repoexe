package com.operators;

public class ArithmeticOperators {

    public static void main(String[] args){
        int a = 10;
        int b = 3;

        System.out.println("a+b = "+(a+b));//o/p : 13
        System.out.println("a-b = "+(a-b));//o/p : 7
        System.out.println("a*b = "+(a*b));//o/p : 30
        System.out.println("a/b = "+(a/b));//return the Quotient value o/p : 3
        System.out.println("a%b = "+(a%b));//return the remainder value o/p : 1
    }
}
